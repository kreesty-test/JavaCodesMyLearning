public class literals01 {
    public static void main(String[] args) {
        int age = 34;
        char ch = 'A';
        short s = 56;
        long agey = 4555555555l;
        float f = 5.5f; // f is literal
        double d1 = 4.66d; // or 4.66
        String str = "lina";
        System.out.println(age);
        System.out.println(str);
        System.out.println(ch);
        System.out.println(s);
        System.out.println(agey);
        System.out.println(f);
        System.out.println(d1);
    }
}
// byte , short, and int all values are stored automatically don't need any
// literals
